#include "libft.h"

void	ft_putnbrmax(intmax_t n)
{
	if (n == -9223372036854775807 - 1)
		write(1, "-9223372036854775808", 20);
	else
	{
		if (n < 0)
		{
			ft_putchar('-');
			n *= -1;
		}
		if (n > 9)
			ft_putnbrmax(n / 10);
		ft_putchar((n % 10) + '0');
	}
}
